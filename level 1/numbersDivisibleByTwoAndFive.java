public class numbersDivisibleByTwoAndFive {

    public static void main(String[] args) {
        
        
        for(int number = 1; number <= 50; number++){
           
            if(number % 2 == 0 && number % 5 == 0 ) {

                System.out.print(number + " ");    

            }            

            

        }  

                     
        
    }

}
